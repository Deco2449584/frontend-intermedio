console.log(5 > 5);
console.log(5 >= 5);
console.log(5 < 5);
console.log(5 <= 5);
// Comparación de valor -> ==
console.log(5 == 5);
console.log(5 == "5"); // true
console.log(0 == false); // true
// Comparación de valor y tipo -> ===
console.log(5 === 5);
console.log(5 === "5");
console.log(5 != "5");
console.log(5 !== "5");
// Negación
console.log(!(5 === 5));

